<script>
function searchJobs() {
    const form = document.getElementById('job-search-form');
    const company = form.elements.company.value;
    const skills = form.elements.skills.value;
    const location = form.elements.location.value;
    const role = form.elements.role.value;
    const includeKeywords = form.elements.include_keywords.value;
    const excludeKeywords = form.elements.exclude_keywords.value;

    const baseUrl = 'https://www.google.com/search?q=';
    const searchQuery = `"Hiring" intitle:"${company}" "${skills}" -"${excludeKeywords}" "${includeKeywords}" "Hiring * ${role}" -intitle:"profiles" -inurl:"dir/ " site:${location}.linkedin.com/${location} OR site:www.linkedin.com/pub`;

    window.open(baseUrl + encodeURIComponent(searchQuery), '_blank');
}
